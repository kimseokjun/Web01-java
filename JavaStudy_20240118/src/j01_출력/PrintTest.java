package j01_출력;

public class PrintTest {

	public static void main(String[] args) {
		System.out.println("김석준");// 출력후 줄바꿈
		System.out.println("남자");	
		System.out.print("김석준1");	// 줄바꿈없이 출력
		System.out.println("김석준2");
		
		System.out.print("김석준3\n");	// \n 은 줄바꿈하라는 용도로 사용
		System.out.println("김석준4");
		
		//주석 : 코드에 영향을 주지않는 부분
		/*
		 * 장문주석
		 * 
		 * 실행단축기 : ctrl + F11
		 */
	}
}
